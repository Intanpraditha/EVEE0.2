function openDatePicker() {
    document.getElementById("hiddenDate").showPicker();
}

// --- Ketika pilih tanggal dari date picker ---
document.getElementById("hiddenDate").addEventListener("change", function() {
    let date = new Date(this.value);

    let d = String(date.getDate()).padStart(2, "0");
    let m = String(date.getMonth() + 1).padStart(2, "0");
    let y = date.getFullYear();

    document.getElementById("day").value = d;
    document.getElementById("month").value = m;
    document.getElementById("year").value = y;
});


// --- Fungsi Cek Period ---
function checkPeriod() {
    let d = parseInt(document.getElementById("day").value);
    let m = parseInt(document.getElementById("month").value);
    let y = parseInt(document.getElementById("year").value);

    let result = document.getElementById("resultText");

    if (!d || !m || !y) {
        result.textContent = "Please enter a valid date.";
        return;
    }

    let lastPeriod = new Date(y, m - 1, d);

    // Prediksi default 28 hari
    let nextPeriod = new Date(lastPeriod);
    nextPeriod.setDate(nextPeriod.getDate() + 28);

    result.textContent = "Your next period is estimated on: " + nextPeriod.toDateString();
}


// --- Tombol refresh/reset ---
function resetForm() {
    document.getElementById("day").value = "";
    document.getElementById("month").value = "";
    document.getElementById("year").value = "";
    document.getElementById("hiddenDate").value = "";
    document.getElementById("resultText").textContent = "";
}
</script>



